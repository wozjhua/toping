const uint8_t* FindHudGlyph(char c) {
    for (const auto& g : kHudGlyphs) {
        if (g.ch == c) return g.rows;
    }
    return kHudGlyphs[sizeof(kHudGlyphs) / sizeof(kHudGlyphs[0]) - 1].rows;
}

void HudSetPixel(std::vector<uint8_t>& pixels, int texW, int texH, int x, int y,
                        uint8_t b, uint8_t g, uint8_t r, uint8_t a) {
    if (x < 0 || y < 0 || x >= texW || y >= texH) return;
    size_t idx = (size_t(y) * size_t(texW) + size_t(x)) * 4u;
    pixels[idx + 0] = b;
    pixels[idx + 1] = g;
    pixels[idx + 2] = r;
    pixels[idx + 3] = a;
}

void HudFillRect(std::vector<uint8_t>& pixels, int texW, int texH, int x, int y, int w, int h,
                        uint8_t b, uint8_t g, uint8_t r, uint8_t a) {
    for (int yy = 0; yy < h; ++yy) {
        for (int xx = 0; xx < w; ++xx) {
            HudSetPixel(pixels, texW, texH, x + xx, y + yy, b, g, r, a);
        }
    }
}

int HudDrawText(std::vector<uint8_t>& pixels, int texW, int texH, int x, int y,
                       const char* text, int scale,
                       uint8_t b, uint8_t g, uint8_t r, uint8_t a) {
    int cursorX = x;
    for (const char* p = text; *p; ++p) {
        const uint8_t* rows = FindHudGlyph(*p);
        for (int row = 0; row < 7; ++row) {
            for (int col = 0; col < 5; ++col) {
                if (rows[row] & (1 << (4 - col))) {
                    HudFillRect(pixels, texW, texH,
                                cursorX + col * scale,
                                y + row * scale,
                                scale, scale,
                                b, g, r, a);
                }
            }
        }
        cursorX += 6 * scale;
    }
    return cursorX - x;
}
